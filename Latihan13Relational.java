public class Latihan13Relational {

    public static void main(String[] args) {

        int A = 10;
        int B = 20;

        System.out.println("a == b = " + (A == B));
        System.out.println("a != b = " + (A != B));
        System.out.println("a > b = " + (A > B));
        System.out.println("a < b = " + (A < B));
        System.out.println("a >= b = " + (B >= A));
        System.out.println("a <= b = " + (B <= A));

    }

}
